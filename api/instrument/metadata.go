// InstrumentDate -> Metadata
type Metadata struct {
	code              string
	name              string
	description       string
	minStartDate      string
	decimalFactor     float64
	minStartDateDaily string
	group             string
}

func (m Metadata) Code() string {
	return m.code
}

func (m Metadata) Name() string {
	return m.name
}

func (m Metadata) Description() string {
	return m.description
}

func (m Metadata) MinStartDate() string {
	return m.minStartDate
}

func (m Metadata) DecimalFactor() float64 {
	return m.decimalFactor
}

func (m Metadata) MinStartDateDaily() string {
	return m.minStartDateDaily
}

func (m Metadata) Group() string {
	return m.group
}

// Returns instrument with requested code.
//
// Returns [nil] if not found
func GetMetadata(code string) *Metadata {
	symbol, ok := codeToSymbol[strings.ToLower(code)]
	if ok {
		return &symbol
	}
	return nil
}

